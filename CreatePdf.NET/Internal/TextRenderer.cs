namespace CreatePdf.NET.Internal;

internal static class TextRenderer
{
    public const int CharWidth = 8;
    public const int CharHeight = 12;
    private const int CharSpacing = 1;
    private const int Padding = 10;

    public static BitmapImage RenderBitmap(ReadOnlySpan<char> text, Dye textDye, Dye backgroundDye, int scale = 3)
    {
        var validChars = ExtractRenderableChars(text);
        var (width, height) = CalculateDimensions(validChars.Length, scale);
        var bitmap = RenderToBitmap(validChars, width, height, scale);
        return BitmapImage.FromBitmap(bitmap, width, height, textDye, backgroundDye);
    }

    internal static (int Width, int Height) CalculateDimensions(int charCount, int scale)
    {
        var textWidth = charCount * (CharWidth + CharSpacing) * scale - CharSpacing * scale;
        return (Math.Max(textWidth, 0) + Padding * 2, CharHeight * scale + Padding * 2);
    }

    private static char[] ExtractRenderableChars(ReadOnlySpan<char> text)
    {
        using var rental = MemoryPool<char>.Shared.Rent(text.Length);
        var buffer = rental.Memory.Span;
        var count = 0;

        foreach (var ch in text)
            if (Glyphs.ContainsKey(ch))
                buffer[count++] = ch;

        return buffer[..count].ToArray();
    }

    private static bool[] RenderToBitmap(ReadOnlySpan<char> chars, int width, int height, int scale)
    {
        var bitmap = new bool[width * height];
        var x = Padding;

        foreach (var ch in chars)
        {
            if (Glyphs.TryGetValue(ch, out var glyph) || Glyphs.TryGetValue(char.ToUpperInvariant(ch), out glyph))
                RenderGlyph(bitmap, width, glyph, x, Padding, scale);

            x += (CharWidth + CharSpacing) * scale;
        }

        return bitmap;
    }

    internal static void RenderGlyph(bool[] bitmap, int width, byte[] glyph, int x, int y, int scale)
    {
        for (var row = 0; row < CharHeight; row++)
        {
            var glyphRow = glyph[row];
            for (var col = 0; col < CharWidth; col++)
                if ((glyphRow & (1 << (CharWidth - 1 - col))) != 0)
                    FillPixel(bitmap, width, x + col * scale, y + row * scale, scale);
        }
    }

    private static void FillPixel(bool[] bitmap, int width, int x, int y, int scale)
    {
        for (var dy = 0; dy < scale; dy++)
        for (var dx = 0; dx < scale; dx++)
        {
            var pixelIndex = (y + dy) * width + x + dx;
            if (pixelIndex < bitmap.Length) bitmap[pixelIndex] = true;
        }
    }

